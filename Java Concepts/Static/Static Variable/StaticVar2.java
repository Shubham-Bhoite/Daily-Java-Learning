class StaticVar2{
	static byte a;
	static short b;
	static int c;
	static long d;
	static double e;
	static float f;
	static char g;
	static boolean h;

	public static void main(String[] args) {
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}
}