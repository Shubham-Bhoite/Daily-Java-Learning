class ForLoop3{
	public static void main(String[] args) {
		for(char ch='A'; ch<='Z'; ch++){
			System.out.println(ch+" ");
		}
		System.out.println("****************************************");

		for(char ch=97; ch<=122; ch++){
			System.out.println(ch+" ");
		}
		System.out.println("***************************************");

		for(char ch=48; ch<='9'; ch++){
			System.out.println(ch+" ");
		}
	}
}