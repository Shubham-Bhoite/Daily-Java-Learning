class ByteExample2
{
	public static void main(String []args)
	{
		byte noBlocks = 4;
		byte elementsDblock = 40;
		byte elementsFblock = 28;
		byte elementsPblock = 35;
		byte elementsSblock = 14;


		System.out.println("No. of Blocks =" + noBlocks);
		System.out.println("Elements in DBlocks =" + noBlocks);
		System.out.println("Elements inFBlocks =" + elementsFblock);
		System.out.println("Elements in PBlocks =" + elementsPblock);
		System.out.println("Elements in SBlocks =" + elementsSblock);
	}
}
